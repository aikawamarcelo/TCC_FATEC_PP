    private void TABUsuariosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TABUsuariosKeyReleased
      if (TABUsuarios.getSelectedRow() != -1) {
           TBCPF.setText(TABUsuarios.getValueAt(TABUsuarios.getSelectedRow(), 0).toString());
            TBLogin.setText(TABUsuarios.getValueAt(TABUsuarios.getSelectedRow(), 1).toString());
             TBSenha.setText(TABUsuarios.getValueAt(TABUsuarios.getSelectedRow(), 2).toString());
             CBPerfil.setSelectedItem(TABUsuarios.getValueAt(TABUsuarios.getSelectedRow(), 3).toString());
    
                    BCadastrar.setEnabled(false);
                    BAlterar.setEnabled(true);
                    BExcluir.setEnabled(true);

        } 
    }//GEN-LAST:event_TABUsuariosKeyReleased

