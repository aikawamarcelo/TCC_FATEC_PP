    private void BExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BExcluirActionPerformed
        if (TABUsuarios.getSelectedRow() != -1) {
         int confirma = JOptionPane.showConfirmDialog(null,"Confirma a exclusão deste Usuário? ","Atenção",JOptionPane.YES_NO_OPTION);
        if (confirma== JOptionPane.YES_OPTION){
       UsuarioDAO dao = new UsuarioDAO();
       Usuario u = new Usuario();
       
       u.setCpf((String) TABUsuarios.getValueAt(TABUsuarios.getSelectedRow(),0));
       dao.delete(u);
         TBCPF.setText("");
         TBLogin.setText("");
         TBSenha.setText("");
        readJTable();
        }
        else{
        
        
        }
        }
       else {
            JOptionPane.showMessageDialog(null, "Selecione um Usuário para excluir.");
        }
       
       
       
       
    }//GEN-LAST:event_BExcluirActionPerformed

    private void BAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAlterarActionPerformed
       if (TABUsuarios.getSelectedRow() != -1) {
  int confirma = JOptionPane.showConfirmDialog(null,"Confirma a alteração deste Usuário? ","Atenção",JOptionPane.YES_NO_OPTION);
        if (confirma== JOptionPane.YES_OPTION){
            
           if(TBCPF.getText().isEmpty()||TBLogin.getText().isEmpty()||TBSenha.getText().isEmpty()){
               
               JOptionPane.showMessageDialog(null,"Preencha todos os campos obrigatórios"); 
              } 
            
           else{
               
             Usuario u = new  Usuario();
             UsuarioDAO dao = new  UsuarioDAO();

            u.setLogin(TBLogin.getText());
            u.setSenha(TBSenha.getText());
            u.setPerfil(CBPerfil.getSelectedItem().toString());
           
            u.setCpf((String) TABUsuarios.getValueAt(TABUsuarios.getSelectedRow(), 0));
            
            dao.update(u);

            TBLogin.setText("");
            TBSenha.setText("");
            TBCPF.setText("");
           readJTable();
           }
           

        }
       
        else{
        
        }
       }
       else{
         JOptionPane.showMessageDialog(null, "Selecione um Usuário para alterar");
       }
    }//GEN-LAST:event_BAlterarActionPerformed

