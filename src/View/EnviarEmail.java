public void pesquisarPaciente() {
   
        DefaultTableModel modelo = (DefaultTableModel) TABEMAILPACIENTES.getModel();
        modelo.setNumRows(0);
        PacienteDAO pdao  = new PacienteDAO();

        for (Paciente p : pdao.pesquisarDadosPaciente()){

            modelo.addRow(new Object[]{
                p.getNome(),
                p.getEmail()
                   
            });

        }

    }
