    private void BEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEnviarActionPerformed

     SimpleEmail email = new SimpleEmail();
      email.setSSLOnConnect(true);
      email.setHostName(TBEndereco.getText());
       email.setSslSmtpPort(TBPorta.getText());
    
      email.setAuthenticator(new DefaultAuthenticator(TBEmailp.getText(),TBSenha.getText()));
      email.setSSLOnConnect(true);
      try{
        email.setFrom(TBDestino.getText());
        email.setDebug(true);
        email.setSubject(TBAssunto.getText());
       email.setMsg(TBMensagem.getText());
       email.addTo(TBDestino.getText());
        email.send();
        JOptionPane.showMessageDialog(null,"E-mail enviado com sucesso!");
        
        
    }catch(EmailException e){
    
        JOptionPane.showMessageDialog(null,"Não foi possível enviar o e-mail, tente novamnete!");
    
    
    }

   
      
    }//GEN-LAST:event_BEnviarActionPerformed

